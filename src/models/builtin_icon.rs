#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn all_icons_have_unique_names() {
        let mut names: Vec<&str> = BUILTIN_ICONS.iter().map(|i| i.name).collect();
        let total = names.len();
        names.sort_unstable();
        names.dedup();
        assert_eq!(names.len(), total, "duplicate icon names found");
    }

    #[test]
    fn find_existing_icon() {
        assert!(find_builtin_icon("server").is_some());
        assert!(find_builtin_icon("database").is_some());
    }

    #[test]
    fn find_missing_icon_returns_none() {
        assert!(find_builtin_icon("nonexistent").is_none());
    }
}
